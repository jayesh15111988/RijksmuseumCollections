import UIKit

// A styles enum to store all the styles such as padding, spacing, corner radius etc.
enum Style {

    //MARK: Paddings
    enum Padding {
        static let defaultHorizontal: CGFloat = 16.0
        static let defaultVertical: CGFloat = 16.0
        static let smallVertical: CGFloat = 8.0
        static let smallHorizontal: CGFloat = 8.0
    }

    static let cornerRadius: CGFloat = 8.0
}
